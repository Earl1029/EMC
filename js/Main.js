function animateDisc(timestamp) {
    if (!detailsPoster || !detailsPage || detailsPage.classList.contains('hidden')) return;
    if (discRotationStart === null) discRotationStart = timestamp;
    const elapsed = timestamp - discRotationStart;
    const degrees = (elapsed / 30000) * 360;
    detailsPoster.style.transform = `rotate(${degrees}deg)`;
    discAnimationFrame = requestAnimationFrame(animateDisc);
}

function restartDiscAnimation() {
    if (!detailsPoster) return;
    if (discAnimationFrame) cancelAnimationFrame(discAnimationFrame);
    discRotationStart = null;
    detailsPoster.style.animation = 'none';
    detailsPoster.style.transform = 'rotate(0deg)';
    discAnimationFrame = requestAnimationFrame(animateDisc);
}

