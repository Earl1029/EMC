searchInput.addEventListener('keydown', (e) => {
    if (e.key === 'Enter') {
        e.preventDefault();
        const term = searchInput.value.toLowerCase().trim();
        if (term) {
            addToSearchHistory(searchInput.value.trim()); // Store original case for history
            // Perform search directly (case-insensitive and space-tolerant)
            const normalizedTerm = term.replace(/\s+/g, ''); // Remove all spaces
            const results = archives.filter(p => {
                const normalizedTitle = p.title.toLowerCase().replace(/\s+/g, '');
                const normalizedDesc = p.desc.toLowerCase().replace(/\s+/g, '');
                const normalizedGenre = p.genre.toLowerCase().replace(/\s+/g, '');
                const normalizedDir = p.dir.toLowerCase().replace(/\s+/g, '');
                
                return normalizedTitle.includes(normalizedTerm) ||
                       normalizedDesc.includes(normalizedTerm) ||
                       p.cast.some(c => {
                           const normalizedRole = c.role.toLowerCase().replace(/\s+/g, '');
                           const normalizedName = c.name.toLowerCase().replace(/\s+/g, '');
                           return normalizedRole.includes(normalizedTerm) || normalizedName.includes(normalizedTerm);
                       }) ||
                       p.tags.some(t => t.toLowerCase().replace(/\s+/g, '').includes(normalizedTerm)) ||
                       normalizedGenre.includes(normalizedTerm) ||
                       normalizedDir.includes(normalizedTerm) ||
                       // Also keep original spaced search for exact matches
                       p.title.toLowerCase().includes(term) ||
                       p.desc.toLowerCase().includes(term) ||
                       p.cast.some(c => c.role.toLowerCase().includes(term) || c.name.toLowerCase().includes(term)) ||
                       p.tags.some(t => t.toLowerCase().includes(term)) ||
                       p.genre.toLowerCase().includes(term) ||
                       p.dir.toLowerCase().includes(term);
            });
            showSearchResults(results, searchInput.value.trim()); // Show original case in results
        }
    }
});

